package com.suyun.vo;

public class BumenVO {

	private Long id;
	private String mc;
	private Long danwei;
	private String zt;
	private String dwmc;
	public Long getId() {
		return id;
	}
	public String getMc() {
		return mc;
	}
	public Long getDanwei() {
		return danwei;
	}
	public String getZt() {
		return zt;
	}
	public String getDwmc() {
		return dwmc;
	}
	public void setId(Long id) {
		this.id = id;
	}
	public void setMc(String mc) {
		this.mc = mc;
	}
	public void setDanwei(Long danwei) {
		this.danwei = danwei;
	}
	public void setZt(String zt) {
		this.zt = zt;
	}
	public void setDwmc(String dwmc) {
		this.dwmc = dwmc;
	}
	
	
}
